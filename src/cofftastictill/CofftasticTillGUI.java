    private void deleteBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteBtnActionPerformed
        // TODO add your handling code here:
        String cusName = cusNameTf.getText();
        
        Cofftastic c;
        coffcount = aList.size();
        if (cusName.equals("")) {
            JOptionPane.showConfirmDialog(null, "Enter Customer Name");
        } 
        else 
        {
            for (int i = 0; i < coffcount; i++) {
               
                c = aList.get(i);
                if (cusName.equals(c.getCustomer())) 
                {

                    aList.remove(i);

                    JOptionPane.showMessageDialog(null, c.getCustomer() + " Has been Deleted "
                            + "There are" + coffcount + "Customers in the database");
                    break;
                }
            }
        }
        if (coffcount == 0) {
            JOptionPane.showMessageDialog(null, "There is zero Customers in the database");
        }


    }//GEN-LAST:event_deleteBtnActionPerformed

