    private void viewsavedBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_viewsavedBtnActionPerformed
        // TODO add your handling code here:

        File inFile;
        FileInputStream fStream;
        ObjectInputStream oStream;

        try {
            inFile = new File("output.data");
            fStream = new FileInputStream(inFile);
            oStream = new ObjectInputStream(fStream);

            ArrayList<Cofftastic> aList;
            aList = (ArrayList<Cofftastic>) oStream.readObject();
            for (Cofftastic c : aList) {
                JOptionPane.showMessageDialog(null, c.printDetails());
            }
            oStream.close();

        } catch (IOException e) {
            System.out.println(e);

        } catch (ClassNotFoundException ex) {
            System.out.println(ex);
        }
    }//GEN-LAST:event_viewsavedBtnActionPerformed

